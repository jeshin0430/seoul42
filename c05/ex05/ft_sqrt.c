int		ft_sqrt(int nb)
{
	long long res;

	if (nb < 0)
		return (0);
	res = 1;
	while (res * res <= (long long)nb)
	{
		if (res * res == (long long)nb)
			return (res);
		res++;
	}
	return (0);
}
